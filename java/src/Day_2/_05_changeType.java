package Day_2;

public class _05_changeType {

	public static void main(String[] args) {
		int num1 = 11;
		double num2 = 3.14;
		
		double num1Change = (double)num1;
		System.out.println("num1 : " + num1);
		System.out.println("num2 : " + num2);
		System.out.println("num1Chage : " + num1Change);
		System.out.println("num1Chante + num2 : " + (num1Change + num2));
		System.out.println("1.23Chage : " + (int)1.23);
	}

}
